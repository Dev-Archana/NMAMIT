#  1. Inheritance
# Definition:
# Inheritance is a feature in object-oriented programming where one class (child/derived class) can acquire the properties and behaviors (methods and variables) of another class (parent/base/super class).

# 2. Parent Class / Base Class / Super Class
# Definition:
# A parent class is the class whose features (variables and methods) are inherited by another class.
# It is also known as the base or super class.

#  3. Child Class / Derived Class / Subclass
# Definition:
# A child class is the class that inherits the features of the parent class.
# It is also called the derived class or subclass.

#  4. super() Function
# Definition:
# The super() function is used to call the constructor or method of the parent class from within the child class.

#  5. Types of Inheritance in Python
# Single Inheritance: One child class inherits from one parent class.

# Multiple Inheritance: One child class inherits from more than one parent class.

# Multilevel Inheritance: A class inherits from a child class, which itself inherits from another class.

# Hierarchical Inheritance: Multiple child classes inherit from a single parent class.

# Hybrid Inheritance: A combination of two or more types of inheritance.

#  6. Method Overriding
# Definition:
# If a child class defines a method with the same name as one in the parent class, the child class’s version overrides the parent’s version when called from the child class.