# StringConversionMethods.py
s1= "Hello World"
s2='Hello'
print(s1)
print(s2)
print(s1+s2)  # Concatenation 
print(s1*3) # Repetition
# lower()
print(s1.lower())  # Convert to lowercase
# upper()
print(s1.upper())  # Convert to uppercase
# title()
print(s1.title())  # Convert to title case
# swapcase()
print(s1.swapcase())  # Swap case of characters
# capitalize()
print(s1.capitalize())  # Capitalize the first character