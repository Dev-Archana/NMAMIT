# Searching and counting methods in Python
# find, index, count, and more
s='hello world'
print(s)
print(s.find('d')) # Find the first occurrence of 'o'
print(s.index('o')) # Index of the first occurrence of 'o'
print(s.count('o')) # Count occurrences of 'o'
print(s.startswith('h')) # Check if string starts with 'h'
print(s.endswith('u'))# Check if string ends with 'u